function draw() {
  background(255, 255, 255);

  push();
  translate(100, 100);
  for (let y in data) {
    for (let x in data[y]) {
      let pixel = data[y][x];
      if (pixel !== 0) {
        if (pixel === 1) {
          noStroke();
          fill(198, 147, 118);
        } else if (pixel === 2) {
          noStroke();
          fill(238, 207, 147);
        } else if (pixel === 3) {
          noStroke();
          fill(255, 240, 195);
        } else if (pixel === 4) {
          noStroke();
          fill(149, 95, 40);
        }
        rect(x * size, y * size, size, size);
      }
    }
  }
  pop();
}
